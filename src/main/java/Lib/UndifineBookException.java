package Lib;

public class UndifineBookException extends Exception {

	private static final long serialVersionUID = 5845903249919986229L;

	public UndifineBookException() {
		super();
	}

	public UndifineBookException(String message, Throwable cause) {
		super(message, cause);
	}

	public UndifineBookException(String message) {
		super(message);
	}

	public UndifineBookException(Throwable cause) {
		super(cause);
	}
	
	

}
